                                <h2>Caramel : du chat craintif au roi du salon – Notre histoire d'adoption</h2>
                                <p>Quand nous avons poussé la porte du Fanal des Chats pour la première fois, nous ne savions pas que notre vie allait changer. Dans un coin de la pièce, un chat roux aux yeux dorés se cachait derrière un coussin, tremblant au moindre bruit. C'était Caramel, un chat de 3 ans qui avait été trouvé errant et sous-alimenté dans les rues de Liège.</p>
                                <blockquote>
                                    "Les bénévoles nous avaient prévenus : Caramel avait besoin de temps et de patience. Mais quelque chose dans son regard nous a touchés. On a su immédiatement qu'il faisait partie de notre famille."
                                </blockquote>
                                <p>Les premières semaines ont été difficiles. Caramel passait ses journées sous le lit, refusant toute interaction. Nous lui laissions de la nourriture et de l'eau, et nous lui parlions doucement sans jamais le forcer. Les conseils de l'équipe du Fanal des Chats ont été précieux : nous avons appris à respecter son rythme et à créer un environnement sécurisant.</p>

                                <h3>Les premiers signes de progrès</h3>
                                <p>Au bout de trois semaines, Caramel a commencé à sortir de sa cachette quand la maison était calme. Il explorait timidement le salon pendant la nuit, et nous retrouvions le matin des traces de ses aventures nocturnes. Puis, un soir, il s'est approché de nous pendant que nous regardions la télévision. Il s'est assis à deux mètres et nous a observés pendant une heure entière.</p>

